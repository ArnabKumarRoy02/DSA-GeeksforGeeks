class Solution
{
    public:
    //Function to remove duplicates from unsorted linked list.
    Node * removeDuplicates( Node *head) {
        unordered_map<int, int> mp;
        Node* curr = head;
        while (curr != NULL) {
            mp[curr->data]++;
            curr = curr->next;
        }
        curr = head;
        Node* temp = new Node(-1);
        Node* prev = temp;
        
        while (curr) {
            auto it = mp.find(curr->data);
            if (it != mp.end()) {
                Node* a = new Node(it->first);
                prev->next = a;
                prev = a;
                mp.erase(it);
            }
            curr = curr->next;
        }
        return temp->next;
    }
};


