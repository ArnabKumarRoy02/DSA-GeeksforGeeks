/*You are required to complete this method*/
int max_sum(int arr[],int n) {
    int ans = 0,sum = 0,currSum = 0;
    
    for(int i=0;i<n;i++){
        sum+=arr[i];
        currSum+=arr[i]*i;
    }
    
    ans=currSum;
    for(int i = 1; i < n; i++){
         int newSum = currSum - (sum - arr[i-1]) + arr[i-1] * (n-1);
         currSum = newSum;
         ans = max(ans,currSum);
    }
    return ans;
}
